"""
Tetris digit data and piece shape definitions.

Transcribed from the Arduino TetrisAnimation library:
https://github.com/toblum/TetrisAnimation

Each digit (0-9) is defined as a list of falling block instructions.
Each instruction is a tuple: (blocktype, color_index, x_pos, y_stop, num_rot)

- blocktype: 0=Square, 1=L, 2=J(reverse-L), 3=I, 4=S, 5=Z(reverse-S), 6=T(half-cross), 7=Corner
- color_index: index into COLORS
- x_pos: x offset relative to digit origin
- y_stop: y row where block stops (1-16, 16 = bottom)
- num_rot: number of 90-degree clockwise rotations from default orientation
"""

